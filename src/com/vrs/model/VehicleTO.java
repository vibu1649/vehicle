package com.vrs.model;

public class VehicleTO {

	private String Manufacturer_Name;
	private String Ex_Showroom_Price;
	private String Color;
	private String Seating_Capacity;
	private String Branch_Location;
	private String Vehicle_code;
	private String No_of_vehicles_available;
	private String Stock_Lasting_Date;
	private int result;
	
	
	public String getManufacturer_Name() {
		return Manufacturer_Name;
	}
	public void setManufacturer_Name(String manufacturer_Name) {
		Manufacturer_Name = manufacturer_Name;
	}
	public String getEx_Showroom_Price() {
		return Ex_Showroom_Price;
	}
	public void setEx_Showroom_Price(String ex_Showroom_Price) {
		Ex_Showroom_Price = ex_Showroom_Price;
	}
	public String getColor() {
		return Color;
	}
	public void setColor(String color) {
		Color = color;
	}
	public String getSeating_Capacity() {
		return Seating_Capacity;
	}
	public void setSeating_Capacity(String seating_Capacity) {
		Seating_Capacity = seating_Capacity;
	}
	public String getBranch_Location() {
		return Branch_Location;
	}
	public void setBranch_Location(String branch_Location) {
		Branch_Location = branch_Location;
	}
	public String getVehicle_code() {
		return Vehicle_code;
	}
	public void setVehicle_code(String vehicle_code) {
		Vehicle_code = vehicle_code;
	}
	public String getNo_of_vehicles_available() {
		return No_of_vehicles_available;
	}
	public void setNo_of_vehicles_available(String no_of_vehicles_available) {
		No_of_vehicles_available = no_of_vehicles_available;
	}
	public String getStock_Lasting_Date() {
		return Stock_Lasting_Date;
	}
	public void setStock_Lasting_Date(String stock_Lasting_Date) {
		Stock_Lasting_Date = stock_Lasting_Date;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	
	
}
